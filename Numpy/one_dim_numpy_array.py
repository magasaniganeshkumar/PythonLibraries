# Write a Python program to convert a list of numeric value into a one-dimensional NumPy array

# importing the numpy library and change the numpy as a np
import numpy as np
# define one list of elements
list1 = [12.23, 13.32, 100, 36.32]
# printing the list of elements
print("Original List:", list1)
# converting list1 into one-dimensional NumPy array
array1 = np.array(list1)
print("One-dimensional NumPy array: ", array1)
